package P04_Estructuras;

public class CicloWhile {
    public static void main(String[] args) {
        //Valor inicial
        //Condición
        //Incremento
        int numero=1; //valor inicial
        while (numero<=5){ //Condición de repetición
            System.out.println("Hello");
            numero++; //Incremento
        }
        System.out.println("Eso es todo...");
    }
}
